uint8_t getPixel(const unsigned char* buf, int idx) {
    int byteIndex = idx / 2;
    if (idx % 2 == 0) return buf[byteIndex] & 0x0F;
     else return (buf[byteIndex] >> 4) & 0x0F;
}

 

// For 256-color images (8-bit)
void drawImage256(int x, int y, const unsigned char* imageData, int width, int height) {
    for (int py = 0; py < height; py++) {
        for (int px = 0; px < width; px++) {
            int idx = py * width + px;
            uint8_t colorIndex = imageData[idx];
            uint16_t color = palette256[colorIndex];
            dma_display->drawPixel(x + px, y + py, color);
        }
    }
}
 
